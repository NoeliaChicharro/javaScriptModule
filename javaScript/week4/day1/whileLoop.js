// WHILE LOOP
// example of endless while loop
let x = true;
while (x){
  console.log('Hello');
  x = false;
}

console.log('\t');

let counter = 1;
while (counter < 11){
  console.log(counter);
  counter++;
}

console.log('\t');

// DO WHILE LOOP
let n = 0;

do {
  console.log(n);
  n++;
} while (n < 11);
