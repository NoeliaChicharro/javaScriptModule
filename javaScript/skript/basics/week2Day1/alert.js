/** User Feedback **/
/*
   you can get basic user feedback with prompt, confirm and alert
*/

/** Alert **/
alert('Hello There!');
/*
   displays a box with a message
   ans an 'okey' button to remove it
*/

/** Confirm **/
confirm('Are you there?');
/*
   displays a box with a message and 2 buttons
   an 'OK' button and a cancel button
   if ok is pressed, true will be returned
   if cancel is pressed, false will be returned
*/

/** Prompt **/
prompt('Whats your name?');
/*
   displays a box with a message and 2 buttons
   an ok button and a cancel button
   if ok is pressed, the input value will be returned
   if cancel is pressed, a null value will be returned
   you can also ass a second parameter to prompt, which will act as a default value
*/