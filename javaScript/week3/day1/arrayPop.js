// pop
// pop is used to take items out of an array.
var shoppingList = [
    'bananas',
    'apples',
    'pears',
    'oranges',
    'pork',
    'chicken'
];

var lastItem = shoppingList.pop();

console.log(lastItem);
console.log(shoppingList);

// shift
var shifted = shoppingList.shift();
console.log(shifted);
console.log(shoppingList);